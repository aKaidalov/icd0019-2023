package oo.struct;

public class Point3D {
    public Integer x;
    public Integer y;
    public Integer z;
    public Point3D(Integer x, Integer y, Integer z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Integer getX() {
        return this.x;
    }
    public Integer getY() {
        return this.y;
    }
    public Integer getZ() {
        return this.z;
    }
}
